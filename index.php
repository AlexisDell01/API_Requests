      var xhr = new XMLHttpRequest();
      xhr.onreadystatechange = function() {
          if (xhr.readyState === XMLHttpRequest.DONE) {
              if (xhr.status === 200) {
                  var response = JSON.parse(xhr.responseText);
                  var servicesRow = document.getElementById('services-row');
                  if (response && response.length > 0) {
                      response.forEach(function(item) {
                          var colDiv = document.createElement('div');
                          colDiv.className = 'col-lg-4 col-md-12';
                          var servicesDiv = document.createElement('div');
                          servicesDiv.className = 's-single-services text-center';
                          var imgElement = document.createElement('img');
                          imgElement.src = item.url;
                          imgElement.alt = 'img';
                          servicesDiv.appendChild(imgElement);
                          colDiv.appendChild(servicesDiv);
                          servicesRow.appendChild(colDiv);
                      });
                  } else {
                      servicesRow.innerHTML = "<p>No se encontraron datos de im√°genes.</p>";
                  }
              } else {
                  console.error('Hubo un problema al realizar la solicitud.');
              }
